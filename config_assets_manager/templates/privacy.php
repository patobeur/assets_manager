        <h2 class="text-2xl font-semibold text-gray-800 mt-6 mb-2">1. Introduction</h2>
        <p class="mb-4">
            Cette Politique de confidentialité décrit nos politiques et procédures sur la collecte, l'utilisation et la divulgation de vos informations lorsque vous utilisez notre application de gestion de matériel (le "Service"). Nous utilisons vos données personnelles pour fournir et améliorer le Service. En utilisant le Service, vous acceptez la collecte et l'utilisation d'informations conformément à cette Politique de confidentialité.
        </p>

        <h2 class="text-2xl font-semibold text-gray-800 mt-6 mb-2">2. Collecte et utilisation des données</h2>
        <p class="mb-4">
            Nous collectons plusieurs types d'informations dans le but de fournir et d'améliorer notre Service.
        </p>
        <h3 class="text-xl font-semibold text-gray-800 mt-4 mb-2">Types de données collectées :</h3>
        <ul class="list-disc list-inside mb-4">
            <li><strong>Données personnelles :</strong> Lors de l'utilisation de notre Service, nous pouvons vous demander de nous fournir certaines informations personnellement identifiables qui peuvent être utilisées pour vous contacter ou vous identifier. Ces informations peuvent inclure, sans s'y limiter :
                <ul class="list-disc list-inside ml-6">
                    <li>Prénom et nom de famille</li>
                    <li>Adresse e-mail</li>
                    <li>Rôle (Administrateur, Agent)</li>
                </ul>
            </li>
            <li><strong>Données relatives aux étudiants :</strong> Le service stocke des informations sur les étudiants pour gérer les prêts, notamment :
                <ul class="list-disc list-inside ml-6">
                    <li>Prénom et nom de famille</li>
                    <li>Adresse e-mail</li>
                    <li>Promotion et section</li>
                    <li>Code-barres unique</li>
                </ul>
            </li>
            <li><strong>Données d'utilisation :</strong> Les informations sur la manière dont le Service est accédé et utilisé (par exemple, les actions de prêt et de retour, les dates et heures des actions) sont collectées pour surveiller l'utilisation du Service et à des fins d'audit.</li>
        </ul>

        <h2 class="text-2xl font-semibold text-gray-800 mt-6 mb-2">3. Utilisation de vos données</h2>
        <p class="mb-4">
            L'application utilise les données collectées à diverses fins :
            <ul class="list-disc list-inside mb-4">
                <li>Pour fournir et maintenir notre Service.</li>
                <li>Pour gérer les comptes utilisateurs et les autorisations.</li>
                <li>Pour suivre l'inventaire du matériel et l'historique des prêts.</li>
                <li>Pour vous permettre d'utiliser les fonctionnalités interactives de notre Service lorsque vous choisissez de le faire.</li>
                <li>Pour fournir un support client.</li>
                <li>Pour surveiller l'utilisation de notre Service afin de détecter, prévenir et résoudre les problèmes techniques.</li>
            </ul>
        </p>

        <h2 class="text-2xl font-semibold text-gray-800 mt-6 mb-2">4. Sécurité des données</h2>
        <p class="mb-4">
            La sécurité de vos données est importante pour nous, mais n'oubliez pas qu'aucune méthode de transmission sur Internet ou méthode de stockage électronique n'est sécurisée à 100 %. Bien que nous nous efforcions d'utiliser des moyens commercialement acceptables pour protéger vos données personnelles, nous ne pouvons garantir leur sécurité absolue. Nous utilisons des mots de passe hachés et des requêtes préparées pour protéger vos données contre les accès non autorisés et les injections SQL.
        </p>

        <h2 class="text-2xl font-semibold text-gray-800 mt-6 mb-2">5. Conservation des données</h2>
        <p class="mb-4">
            Nous ne conserverons vos données personnelles que le temps nécessaire aux fins énoncées dans la présente Politique de confidentialité. Nous conserverons et utiliserons vos données dans la mesure nécessaire pour nous conformer à nos obligations légales, résoudre les litiges et faire respecter nos politiques.
        </p>

        <h2 class="text-2xl font-semibold text-gray-800 mt-6 mb-2">6. Vos droits en matière de protection des données</h2>
        <p class="mb-4">
            Selon votre juridiction, vous pouvez avoir certains droits concernant vos données personnelles, tels que le droit d'accès, de correction, de suppression ou de restriction du traitement de vos données. Les utilisateurs administrateurs peuvent gérer directement les données des utilisateurs et des étudiants via l'interface de l'application.
        </p>

        <h2 class="text-2xl font-semibold text-gray-800 mt-6 mb-2">7. Modifications de cette Politique de confidentialité</h2>
        <p class="mb-4">
            Nous pouvons mettre à jour notre Politique de confidentialité de temps à autre. Nous vous informerons de tout changement en publiant la nouvelle Politique de confidentialité sur cette page. Il est conseillé de consulter périodiquement cette Politique de confidentialité pour tout changement.
        </p>
